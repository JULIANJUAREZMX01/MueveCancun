import { execFileSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const appVersion = '4.0.0';

function localCommit() {
  try {
    return execFileSync('git', ['rev-parse', 'HEAD'], { cwd: root, encoding: 'utf8' }).trim();
  } catch {
    return 'local';
  }
}

const commit = process.env.VERCEL_GIT_COMMIT_SHA?.trim() || localCommit();
const shortCommit = commit === 'local' ? 'local' : commit.slice(0, 7);
const buildId = `build-${shortCommit}`;
const cacheVersion = `v${appVersion}-${buildId}`;
const buildDate = new Date().toISOString();
const output = resolve(root, 'src/generated/buildInfo.ts');

mkdirSync(dirname(output), { recursive: true });
writeFileSync(output, `// Generated by scripts/generate-build-info.mjs. Do not edit manually.\nexport const APP_VERSION = ${JSON.stringify(appVersion)};\nexport const GIT_COMMIT = ${JSON.stringify(commit)};\nexport const SHORT_COMMIT = ${JSON.stringify(shortCommit)};\nexport const BUILD_ID = ${JSON.stringify(buildId)};\nexport const BUILD_DATE = ${JSON.stringify(buildDate)};\nexport const CACHE_VERSION = ${JSON.stringify(cacheVersion)};\n`);

console.log(`[build-info] ${buildId} (${cacheVersion})`);
